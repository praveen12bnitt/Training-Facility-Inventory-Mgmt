package com.smartworks.invtmgmt.web.ui.transfer;

public class UILaundry {
	private String laundryType;
	private String unitNo;
	private String clientInfo;
	private String time;
	private String weight;
	private String buggyWeight;
	private String itemWeight;
	
	public String getLaundryType() {
		return laundryType;
	}
	public void setLaundryType(String laundryType) {
		this.laundryType = laundryType;
	}
	public String getUnitNo() {
		return unitNo;
	}
	public void setUnitNo(String unitNo) {
		this.unitNo = unitNo;
	}
	public String getClientInfo() {
		return clientInfo;
	}
	public void setClientInfo(String clientInfo) {
		this.clientInfo = clientInfo;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getWeight() {
		return weight;
	}
	public void setWeight(String weight) {
		this.weight = weight;
	}
	public String getBuggyWeight() {
		return buggyWeight;
	}
	public void setBuggyWeight(String buggyWeight) {
		this.buggyWeight = buggyWeight;
	}
	public String getItemWeight() {
		return itemWeight;
	}
	public void setItemWeight(String itemWeight) {
		this.itemWeight = itemWeight;
	}
	
}
